export default function LanguageSwitcher() {
  const selectRef = useRef<HTMLSelectElement>(null);

  useEffect(() => {
    const googleTranslateElement = () => {
      if (window.google?.translate) {
        new window.google.translate.TranslateElement(
          {
            pageLanguage: "en",
            includedLanguages: languages.map((lang) => lang.code).join(","),
            layout:
              window.google.translate.TranslateElement.InlineLayout.HORIZONTAL,
          },
          "google_translate_element"
        );
      }
    };

    window.loadGoogleTranslate = googleTranslateElement;

    if (window.google?.translate) {
      googleTranslateElement();
    }

    return () => {
      if (window.google?.translate) {
        delete window.loadGoogleTranslate;
      }
    };
  }, []);

  const handleLanguageChange = (e: React.ChangeEvent<HTMLSelectElement>) => {
    const langCode = e.target.value;
    if (langCode) {
      const selectField = document.querySelector(
        ".goog-te-combo"
      ) as HTMLSelectElement;
      if (selectField) {
        selectField.value = langCode;
        selectField.dispatchEvent(new Event("change"));
      }
    }
  };

  return (
    <div className="relative">
      <select
        ref={selectRef}
        onChange={handleLanguageChange}
        className="bg-white border border-gray-300 rounded-md px-3 py-1.5 text-sm text-gray-700 focus:outline-none focus:ring-2 focus:ring-blue-500 focus:border-blue-500 min-w-[200px]"
        defaultValue=""
      >
        <option value="" disabled>
          Select Language
        </option>
        {languages.map((lang) => (
          <option key={lang.code} value={lang.code}>
            {lang.name} ({lang.code})
          </option>
        ))}
      </select>
      <div id="google_translate_element" className="hidden"></div>
    </div>
  );
}
